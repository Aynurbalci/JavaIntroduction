public class Calculator {
    public int sum(int num1,int num2){
        return num1+num2;
    }
    public int impact(int num1,int num2){
        return num1*num2;
    }
    public  int extraction(int num1,int num2){
        return num1-num2;

    }
    public int divide(int num1,int num2){
        return num1/num2;
    }
}
